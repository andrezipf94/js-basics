// Rounding and signal handling
Math.abs(); // forces a number to be positive (absolute)
Math.sign(); // returns -1 if its a negative, 1 if positive
Math.ceil(); // rounds towards the closest higher integer
Math.floor(); // rounds towards the closest lowest integer
Math.round(); // rounds up if the decimal part >= .5 and down if the decimal part < .5
Math.trunc(); // returns only the integer part of the number (discards the decimals)

// Math operations
Math.sqrt(); // square root
Math.cbrt(); // cubic root
Math.sin(); // sine
Math.cos(); // cosine
Math.tan(); // tangent
Math.hypot(); // the square root of the sum of squares of its arguments (hypotenuse)
Math.exp(); // Math.E raised to a power
Math.log(); // natural log
Math.pow(); // returns the nth power of the number


Math.min(); // returns the min value of all provided params
Math.max(); // return the max value of all provided params

Math.random(); // returns a random number between 0 and 1