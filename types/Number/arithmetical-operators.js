 1 + 1;
 2 - 1;
 2 / 2;
 2 * 2;
 7 % 2; // remainder

 let number = 1;
 number += 1;
 number -= 1;
 number *= 8;
 number /= 2;
 number %= 2;
 
 number++; // This adds one to the number, yet the returned value is still the number, without the increment.
 ++number; // This adds one to the number and returns the incremented number.
 number--; // The same happens with minus.
 --number; // --